export function selectColor(index: number): string {
  if (index === 0) {
    return "lime";
  }
  if (index === 1) {
    return "black";
  }
  if (index === 2) {
    return "magenta";
  }
  if (index === 3) {
    return "yellow";
  }
  if (index === 4) {
    return "blue";
  }
  if (index === 5) {
    return "red";
  }
  if (index === 6) {
    return "orange";
  }
  if (index === 7) {
    return "purple";
  }
  if (index === 8) {
    return "brown";
  }
  if (index === 9) {
    return "green";
  }
  return "";
}
